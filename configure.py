#!/usr/bin/env python3

###
# Generates build files for the project.
# This file also includes the project configuration,
# such as compiler flags and the object matching status.
#
# Usage:
#   python3 configure.py
#   ninja
#
# Append --help to see available options.
###

import argparse
import sys
from pathlib import Path
from typing import Any, Dict, List

from tools.project import (
    Object,
    ProgressCategory,
    ProjectConfig,
    calculate_progress,
    generate_build,
    is_windows,
)

# Game versions
DEFAULT_VERSION = 0
VERSIONS = [
    "GAMEID",  # 0
]

parser = argparse.ArgumentParser()
parser.add_argument(
    "mode",
    choices=["configure", "progress"],
    default="configure",
    help="script mode (default: configure)",
    nargs="?",
)
parser.add_argument(
    "-v",
    "--version",
    choices=VERSIONS,
    type=str.upper,
    default=VERSIONS[DEFAULT_VERSION],
    help="version to build",
)
parser.add_argument(
    "--build-dir",
    metavar="DIR",
    type=Path,
    default=Path("build"),
    help="base build directory (default: build)",
)
parser.add_argument(
    "--binutils",
    metavar="BINARY",
    type=Path,
    help="path to binutils (optional)",
)
parser.add_argument(
    "--compilers",
    metavar="DIR",
    type=Path,
    help="path to compilers (optional)",
)
parser.add_argument(
    "--map",
    action="store_true",
    help="generate map file(s)",
)
if not is_windows():
    parser.add_argument(
        "--wrapper",
        metavar="BINARY",
        type=Path,
        help="path to wibo or wine (optional)",
    )
parser.add_argument(
    "--dtk",
    metavar="BINARY | DIR",
    type=Path,
    help="path to decomp-toolkit binary or source (optional)",
)
parser.add_argument(
    "--objdiff",
    metavar="BINARY | DIR",
    type=Path,
    help="path to objdiff-cli binary or source (optional)",
)
parser.add_argument(
    "--sjiswrap",
    metavar="EXE",
    type=Path,
    help="path to sjiswrap.exe (optional)",
)
parser.add_argument(
    "--ninja",
    metavar="BINARY",
    type=Path,
    help="path to ninja binary (optional)"
)
parser.add_argument(
    "--verbose",
    action="store_true",
    help="print verbose output",
)
parser.add_argument(
    "--non-matching",
    dest="non_matching",
    action="store_true",
    help="builds equivalent (but non-matching) or modded objects",
)
parser.add_argument(
    "--warn",
    dest="warn",
    type=str,
    choices=["all", "off", "error"],
    help="how to handle warnings",
)
parser.add_argument(
    "--no-progress",
    dest="progress",
    action="store_false",
    help="disable progress calculation",
)
args = parser.parse_args()

config = ProjectConfig()
config.version = str(args.version)
version_num = VERSIONS.index(config.version)

# Apply arguments
config.build_dir = args.build_dir
config.dtk_path = args.dtk
config.objdiff_path = args.objdiff
config.binutils_path = args.binutils
config.compilers_path = args.compilers
config.generate_map = args.map
config.non_matching = args.non_matching
config.sjiswrap_path = args.sjiswrap
config.ninja_path = args.ninja
config.progress = args.progress
config.progress_modules = False
if not is_windows():
    config.wrapper = args.wrapper
# Don't build asm unless we're --non-matching
if not config.non_matching:
    config.asm_dir = None

# Tool versions
config.binutils_tag = "2.42-1"
config.compilers_tag = "20231018"
config.dtk_tag = "v1.6.1"
config.objdiff_tag = "v3.0.0-beta.8"
config.sjiswrap_tag = "v1.2.1"
config.wibo_tag = "0.6.16"

# Project
config.check_sha_path = Path("config") / config.version / "build.sha1"
config.asflags = [
    "-mgekko",
    "--strip-local-absolute",
    "-I include",
    f"-I build/{config.version}/include",
    f"--defsym BUILD_VERSION={version_num}",
]
config.ldflags = [
    "-fp hardware",
    "-nodefaults",
]
if args.map:
    config.ldflags.append("-mapunused")
    config.ldflags.append("-listclosure")  # For Wii linkers

# Use for any additional files that should cause a re-configure when modified
config.reconfig_deps = []

# Optional numeric ID for decomp.me preset
# Can be overridden in libraries or objects
config.scratch_preset_id = None

# Base flags, common to most GC/Wii games.
# Generally leave untouched, with overrides added below.
cflags_base = [
    "-proc gekko",
    "-fp hardware",
    "-enum int",
    "-cpp_exceptions on",
    "-nosyspath",
    "-cwd include",
    "-i include",
    "-i include/context_sdk",
    "-i include/context_std",
    "-enc SJIS",
    '-pragma "cats off"',
    '-pragma "warn_notinlined off"'
]

# Warning flags
if args.warn == "all":
    cflags_base.append("-W all")
elif args.warn == "off":
    cflags_base.append("-W off")
elif args.warn == "error":
    cflags_base.append("-W error")

cflags_opt_debug = [
    *cflags_base,
    "-opt off",
    "-inline off",
    "-g",
    "-DDEBUG",
]

cflags_opt_release = [
    *cflags_base,
    "-O4,p",
    "-inline auto",
    "-ipa file",
    "-DNDEBUG",
]

config.linker_version = "GC/3.0a5.2"

# Helper function for Revolution libraries
def RvlLib(lib_name: str, objects: List[Object]) -> Dict[str, Any]:
    return {
        "lib": lib_name,
        "mw_version": "GC/3.0a5.2",
        "cflags_debug": cflags_opt_debug,
        "cflags_release": cflags_opt_release,
        "objects": objects,
    }

DebugMatching = True                   # Object matches and should be linked
DebugNonMatching = False               # Object does not match and should not be linked
DebugEquivalent = config.non_matching  # Object should be linked when configured with --non-matching

ReleaseMatching = True                   # Object matches and should be linked
ReleaseNonMatching = False               # Object does not match and should not be linked
ReleaseEquivalent = config.non_matching  # Object should be linked when configured with --non-matching


# Object is only matching for specific versions
def MatchingFor(*versions):
    return config.version in versions

config.warn_missing_config = True
config.warn_missing_source = False
config.libs = [
    RvlLib(
        "RVLFaceLib",
        [
            Object(DebugEquivalent,     ReleaseMatching,    "RFL_System.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_NANDLoader.c"),
            Object(DebugNonMatching,    ReleaseNonMatching, "RFL_NANDAccess.c"),
            Object(DebugNonMatching,    ReleaseNonMatching, "RFL_Model.c"),
            Object(DebugEquivalent,     ReleaseMatching,    "RFL_MakeTex.c"),
            Object(DebugNonMatching,    ReleaseNonMatching, "RFL_Icon.c"),
            Object(DebugEquivalent,     ReleaseMatching,    "RFL_HiddenDatabase.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_Database.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_Controller.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_MiddleDatabase.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_MakeRandomFace.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_DefaultDatabase.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_DataUtility.c"),
            Object(DebugNonMatching,    ReleaseNonMatching, "RFL_NWC24.c"),
            Object(DebugMatching,       ReleaseMatching,    "RFL_Format.c"),
        ],
    ),
]


# Optional callback to adjust link order. This can be used to add, remove, or reorder objects.
# This is called once per module, with the module ID and the current link order.
#
# For example, this adds "dummy.c" to the end of the DOL link order if configured with --non-matching.
# "dummy.c" *must* be configured as a Matching (or Equivalent) object in order to be linked.
def link_order_callback(module_id: int, objects: List[str]) -> List[str]:
    # Don't modify the link order for matching builds
    if not config.non_matching:
        return objects
    if module_id == 0:  # DOL
        return objects + ["dummy.c"]
    return objects

# Uncomment to enable the link order callback.
# config.link_order_callback = link_order_callback


# Optional extra categories for progress tracking
# Adjust as desired for your project
config.progress_categories = [
    ProgressCategory("release", "Release"),
    ProgressCategory("debug", "Debug"),
]
config.progress_each_module = args.verbose
# Optional extra arguments to `objdiff-cli report generate`
config.progress_report_args = [
    # Marks relocations as mismatching if the target value is different
    # Default is "functionRelocDiffs=none", which is most lenient
    "--config functionRelocDiffs=data_value",
]

if args.mode == "configure":
    # Write build.ninja and objdiff.json
    generate_build(config)
elif args.mode == "progress":
    # Print progress information
    calculate_progress(config)
else:
    sys.exit("Unknown mode: " + args.mode)
